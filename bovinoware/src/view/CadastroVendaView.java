   public boolean validaValor(){
    try{
        Double.parseDouble (txtValor.getText());
        return true;
    }catch (Exception e) {
      
       JOptionPane.showMessageDialog(null, "O campo valor pode registrar números inteiros ou cecimais com ( . ) ex: ' 500 ou 499.90'");
       return false;
       }                        
    }   
    public boolean validaPeso(){
        try {
        Integer.parseInt(txtPeso.getText());
        return true;
    } catch (Exception e) {
         JOptionPane.showMessageDialog(null, "O campo peso pode registrar apenas números inteiros ex: ' 500 '");
        return false;
    }
                         
    }   
    public boolean validaData(){
    try{
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");// validação
                    
        Date data1 = sdf.parse(txtData.getText()); 
        
                    
        String dataVenda = data1+"";
        
        return true;
    }catch (ParseException ex) {
       Logger.getLogger(CadastroCompraView.class.getName()).log(Level.SEVERE, null, ex);
       JOptionPane.showMessageDialog(null, "Erro nas datas! use o formato dd/mm/aaaa");
       return false;
       }                        
    } 
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         
        if(!txtNome.getText().isEmpty() && !txtEndereco.getText().isEmpty() && !txtTelefone.getText().isEmpty()){
            if(!txtData.getText().isEmpty() && !txtValor.getText().isEmpty() && !txtPeso.getText().isEmpty()){
                
                boolean dataValida = validaData();
                boolean validaValor = validaValor();
                boolean validaPeso = validaPeso();
                
                if(dataValida == true && validaValor == true && validaPeso == true ){
                    
                    try{   
                    Bovino bovino = new Bovino();
                    
                    bovino.setDataVenda(txtData.getText());
                    bovino.setPesoVenda(parseInt (txtPeso.getText()));
                    bovino.setValorVenda(parseDouble (txtValor.getText()));
                    bovino.setVendido(true);
                    
                    bc.registraVenda(bovino, getIdBovino());
                    
                    Venda venda = new Venda();
                    
                    
                    venda.setIdUsuario(getIdUser());
                    venda.setIdBovino(getIdBovino());
                    venda.setNome(txtNome.getText());
                    venda.setTelefone(txtTelefone.getText());
                    venda.setEndereco(txtEndereco.getText());
                    
                    vc.salvar(venda);
                    this.dispose();
                    } catch(Error erro){
                         JOptionPane.showMessageDialog(null, "Preenxe os campos CORRETAMENTE! campo peso somente use numerosinteiros,");
                    }  
                }   
            }
            else {JOptionPane.showMessageDialog(null, "Preencha TODOS os campos !");}   
            
        }
        else {JOptionPane.showMessageDialog(null, "Preencha TODOS os campos !");}
    
    }//GEN-LAST:event_btnSalvarActionPerformed
